'''
Tipos de int e float

int -> numero inteiro
O tipo int representa qualquer numero positivo ou negativo.
O int sem sinal é considerado positivo

Float -> numero com ponto flutuante
O tipo float representa qualquer numero positivo ou nagativo com ponto flutuante.
Float sem sinal é considerado positivo

A função type mostra o tipo que o Python inferiu ao valor

'''

#Numeros inteiros
print('0 1 2 3 4')

#Numero Float
print(1.2, 1.4)

#A importancia do Type
print(type(1.1))


